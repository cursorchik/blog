<?php

require __DIR__ . '/../vendor/autoload.php';


$db = require __DIR__ . '/../database/config.php';

try {
	$categories = [
		['name' => 'PHP', 'description' => 'Все о языке PHP'],
		['name' => 'JavaScript', 'description' => 'JS, фреймворки, браузеры'],
		['name' => 'Docker', 'description' => 'Контейнеризация и оркестрация'],
		['name' => 'Базы данных', 'description' => 'SQL, MySQL, PostgreSQL'],
		['name' => 'Веб-разработка', 'description' => 'HTML, CSS, фронтенд'],
		['name' => 'Компьютер сайнс', 'description' => 'Узнайте как работает компьтер'],
	];

	$categoryIds = [];
	foreach ($categories as $cat)
	{
		$db->execute("
            INSERT INTO `categories` (`name`, `description`)
            VALUES (:name, :desc)
        ", [
			'name' => $cat['name'],
			'desc' => $cat['description']
		]);
		$categoryIds[] = $db->lastInsertId();
	}

	$titles = [
		'Введение в PHP',
		'Современный JS',
		'Docker',
		'Индексы в MySQL',
		'Адаптивная верстка',
		'Путешествия',
		'Приключения',
		'Компьтер сайнс',
	];
	$postIds = [];
	for ($i = 1; $i <= 15; $i++)
	{
		$title = "Статья $i: " . $titles[array_rand($titles)];
		$description = "Краткое описание статьи $i. Это анонс для главной и ленты категории.";
		$content = "Полный текст статьи $i. Здесь много интересного содержания, которое демонстрирует работу блога. Повторяем текст для объема. Приятно, граждане, наблюдать, как многие известные личности, вне зависимости от их уровня, должны быть преданы социально-демократической анафеме. Таким образом, граница обучения кадров играет важную роль в формировании укрепления моральных ценностей. В рамках спецификации современных стандартов, реплицированные с зарубежных источников, современные исследования лишь добавляют фракционных разногласий и объявлены нарушающими общечеловеческие нормы этики и морали. Учитывая ключевые сценарии поведения, повышение уровня гражданского сознания обеспечивает актуальность существующих финансовых и административных условий. В рамках спецификации современных стандартов, активно развивающиеся страны третьего мира могут быть указаны как претенденты на роль ключевых факторов. Но сплочённость команды профессионалов предполагает независимые способы реализации позиций, занимаемых участниками в отношении поставленных задач. С учётом сложившейся международной обстановки, синтетическое тестирование требует определения и уточнения распределения внутренних резервов и ресурсов. Современные технологии достигли такого уровня, что выбранный нами инновационный путь создаёт необходимость включения в производственный план целого ряда внеочередных мероприятий с учётом комплекса соответствующих условий активизации. Принимая во внимание показатели успешности, высококачественный прототип будущего проекта прекрасно подходит для реализации как самодостаточных, так и внешне зависимых концептуальных решений. Наше дело не так однозначно, как может показаться: реализация намеченных плановых заданий играет определяющее значение для экспериментов, поражающих по своей масштабности и грандиозности.";
		$views = rand(0, 500);
		$createdAt = date('Y-m-d H:i:s', strtotime("-".rand(0,60)." days"));

		$db->execute("
            INSERT INTO `posts` 
                (`title`, `description`, `content`, `image`, `views`, `created_at`)
            VALUES 
                (:title, :desc, :content, :image, :views, :created)
        ", [
			'title'   => $title,
			'desc'    => $description,
			'content' => $content,
			'image'   => null,
			'views'   => $views,
			'created' => $createdAt
		]);
		$postIds[] = $db->lastInsertId();
	}

	foreach ($postIds as $postId) {
		$numCats = rand(1, 3);
		shuffle($categoryIds);
		$selected = array_slice($categoryIds, 0, $numCats);
		foreach ($selected as $catId) {
			try {
				$db->execute("
                    INSERT INTO `post_category` (`post_id`, `category_id`)
                    VALUES (:post, :cat)
                ", [
					'post' => $postId,
					'cat'  => $catId
				]);
			} catch (\Exception $e) {
				// дубликаты пропускаем
			}
		}
	}

	echo "Seeding completed! Added " . count($categoryIds) . " categories and " . count($postIds) . " posts.\n";

}
catch (\Exception $e)
{
	die("Seeding error: " . $e->getMessage());
}